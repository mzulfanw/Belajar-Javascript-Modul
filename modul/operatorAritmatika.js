export function operatorKali(x, y) {
    return `ini adalah hasil kali  =  ${x * y}`;
}

export function operatorBagi(x, y) {
    return `ini adalah hasil bagi = ${x / y} `;
}

export function operatorTambah(x, y) {
    return `ini adalah hasil tambah ${x + y}`;
}

export function operatorKurang(x, y) {
    return `ini adalah hasil kurang ${x - y}`;
}